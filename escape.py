# Single Quote
print('It\'s a beautiful day!')

# Backslash
print('This is a backslash: \\\.')

# New Line
print('Hello\nWorld!')

# Carriage Return
print('Hello\rWorld!')

# Tab
print('Hello\tWorld!')

# Backspace
print('Hello \bWorld!')

# Form Feed
print('Hello \fWorld!')

# Octal value
print('\141\142\143')

# Hex value
print('\x41\x42\x43')
